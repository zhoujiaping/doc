/**
 * @param {Node} root
 * @return {Node}
 */
var connect = function(root) {
	if(root==null)return null
    let q1 = [],q2 = [root],p
    while(q2.length>0){
    	q1 = q2,q2 = []
    	while(q1.length>0){
    		p = q1.shift()
	    	if(q1.length>0)p.next = q1[0]
	    	if(p.left)q2.push(p.left)
            if(p.right)q2.push(p.right)
    	}
    }
    return root
};